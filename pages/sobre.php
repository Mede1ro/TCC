        <p>Bem-vindo ao nosso espaço dedicado à análise e compreensão dos conflitos ambientais na região dos lagos, uma iniciativa desenvolvida como parte do Trabalho de Conclusão de Curso no Instituto Federal do Rio de Janeiro, Campus Campos Arraial do Cabo.</p>
