        <div class="conteudo-conflitos-texto">
        <p>


            <br><br>
            Em suma, a partir dos fatos mencionados, é possível concluir que as construções
            irregulares, mesmo apresentando uma intimidação constante a diversos aspectos
            ambientais e sociais, vão seguir de forma natural por conta da negligência fiscal dos
            responsáveis que deveriam estar realizando seu trabalho de preservação. Por mais
            que haja turismo e geração de empregos nessas áreas, as pessoas deveriam ter o
            conhecimento dos danos que esse tipo de atividade pode causar à natureza e à
            integridade humana, uma vez que com a poluição e destruição dos bens naturais
            esses lugares vão deixar de ser atrações e, consequentemente, todos sairão
            perdendo. Ao enfrentar esses desafios de maneira íntegra, podemos aspirar a
            proteger efetivamente o Parque Estadual da Costa do Sol, garantindo que as
            gerações futuras possam desfrutar de sua beleza e biodiversidade.

            <br><br>
            <span> ÚLTIMA ATUALIZAÇÃO:</span>
            <br><br>
            O Parque Estadual da Costa do Sol (PECS) tem como missão primordial
            garantir a preservação dos remanescentes da Mata Atlântica e dos ecossistemas
            associados, promovendo a recuperação das áreas degradadas nesse ambiente e
            resguardando as populações de animais e plantas nativas. Contudo, a evolução dos
            processos de urbanização e de especulação imobiliária nos municípios de Arraial do
            Cabo, Cabo Frio e Búzios têm prejudicado a proteção desse ecossistema de
            extrema importância. Apesar do PECS ser classificado como uma Unidade de
            Conservação de Proteção Integral, inúmeros relatos indicam a ocorrência de
            construções irregulares e ocupação inadequada do solo nas áreas abrangidas pelo
            Parque.


            <br><br>
            O referido levantamento realizado visou buscar e organizar artigos e/ou
            notícias vinculadas a essa ameaça ambiental ao Parque Estadual da Costa do Sol.
            Desde sua criação, em 2011, a unidade de proteção tem o objetivo de preservar
            toda a biodiversidade nela incluída, contudo, surgem registros datados a partir de
            2017 sobre a ocupação ilegal dessa região, havendo o relatório inaugural afirmando
            a presença de 13 residências irregulares. Todas essas propriedades estão
            atualmente sujeitas a processos judiciais, os quais estão em andamento perante o
            único tribunal de Arraial do Cabo.


            <br><br>


            Ao decorrer de 2017 até 2023, se tem conhecimento de diversas notícias
            sobre ações de órgãos públicos, como prefeitura, polícia e o INEA, para a
            desocupação destas residências irregulares que estão inseridas nos limites do
            Parque. Nesse contexto, é válido citar uma das informações mais atualizadas
            referentes a uma operação de remoção desses indivíduos das áreas protegidas que
            ocorreu em 2021. Naquele ano, o Ministério Público do Estado do Rio de Janeiro
            (MPRJ), através da 1ª Promotoria de Justiça de Tutela Coletiva do Núcleo Cabo
            Frio, moveu uma ação civil solicitando a desocupação e demolição de 96 casas
            irregulares construídas no Parque Estadual da Costa do Sol, localizadas no Distrito
            de Sabiá, em Arraial do Cabo.


            <br><br>

            O registro mais recente encontrado das ações contra essas residências
            ilegais, está publicado em uma notícia de junho de 2023, no Caiçara, distrito de
            Arraial do Cabo. Segundo informações não oficiais, a operação demoliu cerca de 10
            casas inabitadas, e teve o apoio do 25º Batalhão da Polícia Militar, agentes do
            Instituto Estadual do Ambiente (INEA), da Polícia Ambiental, da Superintendência
            de Combate a Crimes Ambientais (SUPACCA), e da Área de Proteção Ambiental
            (Apa) de Massambaba e do Parque Estadual da Costa do Sol. Entretanto, faz-se
            indispensável evidenciar que não há um fácil acesso aos registros oficiais com
            atualizações dos casos de uso e ocupação ilegal dessa região - isso quando há
            registros - o que resulta na invisibilização de um tema essencial para o bem estar da
            população e proteção dos ecossistemas. Além disso, é imprescindível expor que há
            poucas menções a casos ocorridos nos outros municípios que o Parque engloba,
            tais quais Búzios e Cabo Frio.
            <br><br>

            <span>CONSTRUÇÕES:</span>
            <br><br>
            Construções irregulares representam uma ameaça iminente ao ecossistema
            do Parque Estadual da Costa do Sol em Monte Alto, Arraial do Cabo. A paisagem, outrora intocada, enfrenta, agora, o desafio crescente de construções ilegais, que comprometem a biodiversidade e a sustentabilidade ambiental da região. Essas moradias clandestinas, muitas vezes erguidas sem a devida análise ambiental, colocam em risco ecossistemas delicados e podem resultar em impactos irreversíveis.

