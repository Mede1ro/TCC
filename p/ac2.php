        <h1 class="conteudo-conflitos-titulo">Casas Irregulares Ameaçam Parque Estadual da Costa do Sol na Região dos Lagos</h1>

        <div class="conteudo-conflitos-texto">
            <p>

                <br><br>
                Construções irregulares representam uma ameaça iminente ao
                ecossistema do Parque Estadual da Costa do Sol em Monte Alto, Arraial do Cabo. A
                paisagem, outrora intocada, enfrenta, agora, o desafio crescente de construções
                ilegais, que comprometem a biodiversidade e a sustentabilidade ambiental da
                região. Outrossim, essas moradias clandestinas, muitas vezes erguidas sem a
                devida análise ambiental, colocam em risco ecossistemas delicados e podem
                resultar em impactos irreversíveis devido à sua "proliferação".

                <br><br><br><br>

                A ausência de fiscalização torna-se evidente, já que as últimas atualizações sobre a
                temática, relacionada à desocupação de casas irregulares, foi em junho de 2021, o
                qual representa uma significativa falha governamental e uma defasagem da atenção
                midiática para a problemática. Ademais, a falta de fiscalização impacta,
                negativamente, a eficácia das políticas de conservação. Tornando imprescindível o
                fortalecimento da legislação, a implementação de ações punitivas contra a violação
                e o aumento da conscientização popular sobre a importância da preservação do
                Parque. Vale ressaltar que, o conflito, embora restrito, não envolve apenas questões
                ambientais, já que além de desafiar as políticas de preservação afetam, também, o
                bem-estar social.

            </p>

        </div>

