            <p>

                <br><br>
                As pessoas envolvidas na ocupação irregular dessas áreas abrangem uma
                variedade de atores, cada um contribuindo para a complexidade do problema. Os
                ocupantes da área são divididos entre: *Moradores locais;* muitas vezes movidos
                pela necessidade habitacional urgente, falta de recursos financeiros e a escassez
                de moradias acessíveis à classe trabalhadora (o que a deixa exposta à violência, ao
                tráfico e a condições insalubres de vida), *Locatários;* a procura de dinheiro às
                custas da conservação ambiental. Geralmente não moram no local, mas possuem
                um ou mais imóveis à "beira-mar", *Comerciantes / donos de quiosques;*
                responsáveis por gerar empregos a população local, visto que em alta temporada a
                cidade fica cheia de veraneantes, e *Turistas;* a natureza preservada cria um
                ambiente propício para atividades ao ar livre, mas, levando em conta o contexto
                ilegal de construções, o local se torna alvo daqueles que procuram por "baratear"
                suas estadias na cidade turística de Arraial do Cabo.

                <br><br>
                É importante ressaltar que, as consequências da ocupação do parque Estadual
                Costa Do Sol são vastas. Para além da degradação ambiental, há impactos sociais,
                econômicos e culturais. A perda da biodiversidade pode afetar negativamente a
                economia e a existência de comunidades locais dependentes dos recursos naturais.
                Do mesmo modo, a atividade turística da região também pode ser prejudicada,
                comprometendo a fonte de renda e os empregos associados à natureza. Além do
                problema ambiental alarmante as organizações também devem enfrentar o desafio
                de conciliar a proteção ambiental com as necessidades habitacionais das
                comunidades locais, o que exige abordagens equilibradas e coerentes com o
                conceito de conservação.

                <br><br>

            </p>
