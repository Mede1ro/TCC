            <br>
            em Araruama e seus impactos em São Pedro</h1>

        <div class="conteudo-conflitos-texto">
            <p>

                <br><br>
                O Ministério Público Federal em São Pedro da Aldeia, no Rio de Janeiro (MPF/RJ), firmou termos de
                ajustamento de conduta (TACs) com prefeituras da Região dos Lagos para a desocupação, interdição e
                demolição de quiosques irregularmente instalados nas praias com a remoção dos entulhos provenientes. Os
                Municípios de Búzios, Arraial do Cabo e Cabo Frio se comprometeram a retirar 64 construções irregulares
                logo após o carnaval. O acordo ocorre paralelo à decisão judicial, em ação movida pelo MPF/RJ, que já
                havia determinado a imediata retirada dos quiosques.
                <br><br>
                Os proprietários dos quiosques na Praia do Sol, em São Pedro da Aldeia, estão buscando alternativas para
                reaver a permanência de suas atividades no local. No fim de agosto deste ano, a Prefeitura agendou uma
                reunião com os permissionários e, na ocasião, os informou que o Ministério Público havia pedido a
                desocupação da área. Mas como não ficou claro de quem partiu a exigência de retirada dos quiosques, os
                quiosqueiros entraram com ações judiciais contra o município pedindo explicações.
