        <h1 class="conteudo-conflitos-titulo">DESCARTE IRREGULAR DE RESÍDUOS SÓLIDOS DE SAÚDE NA CIDADE DE CABO FRIO
        </h1>

        <div class="conteudo-conflitos-texto">
            <p>

                <br><br>
                Em julho de 2020, a equipe de coleta da Comsercaf encontrou uma caixa contendo
                seringas, agulhas e frascos de medicamentos usados, descartados de maneira inadequada no
                Centro de Cabo Frio. A responsabilidade pelo descarte irregular recaiu sobre uma clínica
                odontológica identificada, que foi notificada pela Fiscalização da autarquia. No mesmo ano, em
                30 de novembro, a COMSERCAF descobriu seringas, agulhas e embalagens de medicamentos
                descartados de forma irregular no distrito de Tamoios, Cabo Frio, na Região dos Lagos do Rio. O
                presidente da autarquia, Dário Guagliardi, assegurou que os órgãos competentes foram
                acionados, todas as medidas cabíveis foram tomadas, e o material recolhido foi incinerado em
                um aterro sanitário. Em junho de 2021, uma moradora do bairro Passagem-Cabo Frio denunciou
                o descarte inadequado de resíduos hospitalares, incluindo seringas, agulhas, embalagens
                usadas de soro, entre outros materiais, na calçada da Rua Maestro Clodomiro Oliveira. A
                denúncia apontava o hospital Santa Izabel como responsável, pois os itens estavam em frente
                ao Onkosol, ligado ao referido hospital.


                <br><br>
                Segundo a Companhia, é necessário um cadastro prévio para a coleta hospitalar, que atualmente
                atende a mais de 100 unidades médicas registradas. Nas principais clínicas e hospitais do
                município, a coleta ocorre diariamente. Em ambulatórios menores, o recolhimento é programado
                de acordo com as necessidades individuais. Contudo, em casos de descarte irregular de
                resíduos sólidos de saúde, a Comsercaf não possui servidores com autoridade significativa,
                tornando responsabilidade da Secretaria de Meio Ambiente de Cabo Frio e da Vigilância
                Sanitária fiscalizar hospitais, clínicas e lidar com denúncias de aterros sanitários irregulares.

                <br><br>
            
            </p>

        </div>
