        <h1 class="conteudo-conflitos-titulo">O conflito entre turismo e pesca na Reserva Extrativista de Arraial do
            Cabo</h1>

        <div class="conteudo-conflitos-texto">
            <p>

                <br><br>
                A cidade de Arraial do Cabo é conhecida por seu turismo, chegando a receber 200 mil visitantes em épocas
                comemorativas. O enfoque de tantas visitas são as praias, conhecidas por sua beleza natural. No entanto,
                em Arraial do Cabo não há apenas o turismo, também há a pesca artesanal, que por muitas vezes é
                desconhecida por aqueles que visitam a cidade.
                A pesca artesanal vem sendo migrada desde que a álcalis chegou e se instalou em Arraial. Com a ajuda da
                industrialização, a chegada dessa enorme empresa trouxe uma modernização da cidade e uma busca dos
                habitantes por empregos mais sustentáveis. Os ferros de passar antigos e pesados foram substituídos por
                modelos mais práticos, telefones, balanças, máquinas de escrever, e vários outros objetos chegaram a
                Arraial junto com a industrialização.

                <br><br>
                Não só o povo, mas a cultura também acabou sendo industrializada. Agora, a pesca na Canoa de borçada não
                poderia competir com os barcos a motor. As mulheres que antes eram responsáveis pelas rendas das roupas,
                ensinando a arte de mãe para filha, já não conseguiam competir com a velocidade das máquinas que faziam
                metros e metros de renda por dia, enquanto uma mulher levaria meses e meses.
                <br><br>


            </p>

        </div>


        <img class="imagem-conflitos" src="">


        <div class="conteudo-conflitos-texto">

            <p>

                <br><br>
                A vinda da Álcalis acelerou o processo de industrialização da cidade, mas depois que a empresa faliu,
                uma vez que seu produto principal começou a ser vendido por um preço mais barato por outra empresa, o
                setor turístico começou a ser intensamente explorado. Mesmo depois que a oportunidade de trabalhar em
                uma grande empresa desapareceu, a população não se preocupou em retornar às atividades tradicionais, e
                em vez disso, grande parte buscou uma oportunidade de renda no turismo, achando um caminho mais
                agradável e viável. E até nos dias de hoje, as pessoas que moram em Arraial buscam e acham suas fontes
                de renda no turismo, até mesmo pessoas que nascem em famílias mais tradicionais estão se afastando desse
                estilo de vida, proporcionando assim uma escassez de pessoas que sabem pescar tradicionalmente.

                <br><br>
                As gerações de pescadores que se encontram no ano de 2022, podem ser as últimas gerações de pescadores
                tradicionais de Arraial do Cabo. O crescimento massivo do turismo, e a forma como ele se apresentou mais
                seguro (mesmo não sendo, por diversos motivos, um deles sendo a sensacionalidade), além de uma ampla
                opção de serviços que podem ser prestados, acabou ofuscando a presença da pesca artesanal. Esse
                ofuscamento gerou uma gigantesca falta de conhecimento, tanto para as pessoas que vêm para visitar e vão
                embora sem saber que dessa cultura, quanto para pessoas que moram em Arraial e se esqueceram que sua
                cidade foi moldada pelo esforço de diversos pescadores artesanais.
                <br><br>
                Essa falta de reconhecimento vem prejudicando a pesca tradicional, ao ponto de começar a marginalizá-la.
                Um dos problemas que podemos citar é a mudança da paisagem, esse recurso vem sendo explorado cada vez
                mais, ainda mais nas áreas litorais para construção de orlas, o que acaba causando vários conflitos com
                as populações tradicionais. Faz-se necessário o reconhecimento dessas práticas, assim como o
                reconhecimento do território que pertence a essa população tradicional, para evitar futuros conflitos e
                haver a preservação das tradições.
                <br><br>
                “No Arraial do Cabo, ausentes da civilização, os pescadores vivem (...) subordinados às leis que eles
                próprios criaram” Uma das falas do curta-metragem sobre Arraial do Cabo. Intitulado ''Arraial do Cabo’’
                realizado por Mário Carneiro e Paulo Saraceni, em 1959. Fala essa que indica a falta de visibilidade que
                os pescadores possuem, a partir do momento em que o turismo se tornou o centro das atenções.

