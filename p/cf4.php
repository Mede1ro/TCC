                <br><br>

                
                Conforme informações de profissionais do Hospital Otime Cardoso dos Santos, localizado no
                bairro Jardim Esperança em Cabo Frio, todo o lixo gerado pela unidade de saúde é recolhido
                diariamente pelas auxiliares de serviços gerais, tanto pela manhã quanto à noite. Esses
                materiais são coletados pelo carro de coleta da COMSERCAF e são descartados no aterro
                sanitário Dois Arcos

                <br><br>
                O descarte apropriado do lixo hospitalar requer uma abordagem cuidadosa para minimizar
                impactos ambientais e riscos à saúde. O rigoroso cumprimento das diretrizes estabelecidas
                pelo PGRSS e CONAMA contribui para um manejo seguro e responsável dos resíduos
                hospitalares. Itens como seringas, agulhas e produtos químicos, se descartados
                inadequadamente, podem contaminar solo e água, representando riscos para trabalhadores e
                catadores. Segundo PGRSS e CONAMA, o lixo hospitalar deve ser categorizado em resíduos
                comuns, especiais e perigosos, cada um exigindo um tratamento específico para mitigar
                impactos ambientais e riscos à saúde pública. O PGRSS deve orientar as práticas adequadas de
                segregação, acondicionamento, coleta, transporte e destinação final, seguindo as normativas do
                CONAMA.

                <br><br>
                <span>ÚLTIMA ATUALIZAÇÃO: Janeiro de 2023</span>
                <br><br>
                Em janeiro de 2023, o portal Cabo Frio em foco, recebeu uma série de denúncias relacionadas a
                um lixão a céu aberto, no bairro Monte Alegre 2. Atualmente, este local está arrendado a uma
                empresa que tem como único objetivo a sua utilização como depósito de resíduos de
                construção, mas por falta de fiscalização e comodidade por parte da entidade que deveria gerir
                o ambiente, os resíduos orgânicos e a proliferação de lixões a céu aberto para descarte de
                resíduos hospitalares estão causando sérios danos ao meio ambiente e à saúde dos
                moradores de áreas próximas. Diversas vezes os moradores foram coagidos e ameaçados para
                não fazer a denúncia, por fato de existir “peixe grande” nesse esquema e agora são obrigados a
                conviver com esse lixão ao lado de suas casas, causando sérios danos à sua saúde e a saúde
                de sua família.
                <br><br>
                “No Arraial do Cabo, ausentes da civilização, os pescadores vivem (...) subordinados às leis que eles
                próprios criaram” Uma das falas do curta-metragem sobre Arraial do Cabo. Intitulado ''Arraial do Cabo’’
                realizado por Mário Carneiro e Paulo Saraceni, em 1959. Fala essa que indica a falta de visibilidade que
                os pescadores possuem, a partir do momento em que o turismo se tornou o centro das atenções.

