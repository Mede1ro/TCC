                <br><br>
                Em Cabo Frio, a construção do Resort Peró estava prevista para ocorrer dentro da Área de Proteção
                Ambiental (APA) do Pau Brasil.
                As licenças para a construção do Resort haviam sido concedidas pelo INEA e pela prefeitura municipal de
                Cabo Frio. Porém, em ação civil pública movida pelo Ministério Público Federal (MPF), a 1ª Vara Federal
                de São Pedro da Aldeia, no Rio de Janeiro, determinou o imediato cumprimento de sentença para que o
                Instituto Estadual do Ambiente não expeça quaisquer novas licenças para a construção no empreendimento
                Resort Peró, dentro da Área de Proteção Ambiental (APA) do Pau Brasil, em Cabo Frio (RJ), reconhecendo a
                nulidade das já expedidas. Já as sociedades empresárias responsáveis pelo empreendimento deveriam
                promover a imediata demolição das construções porventura efetuadas com base nas novas licenças
                ambientais, com a retirada dos respectivos entulhos e a reparação ambiental. (cumprimento de sentença nº
                5001011-17.2022.4.02.5108/RJ)


                <br><br>
