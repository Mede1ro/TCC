            <p>

                <br><br>
                MPF convoca audiência pública para debater o projeto de ampliação da área de estacionamento do Shopping
                Park Lagos conforme o seguinte edital: <a href="https://www.mpf.mp.br/rj/sala-de-imprensa/docs/prm-sao-pedro-da-aldeia/edital-convocacao-audiencia-publica-estacionamento-park-shopping-cabo-frio/view">Edital
                    Convocação Audiência Pública Estacionamento Park ... - MPF </a>

                <br><br>
                O intuito do shopping ao solicitar a ampliação do estacionamento seria de alcançar a quase 1500 vagas
                com a adição de 370 vagas dessa área que seria irregular. Além de servir de estacionamento, também seria
                destinado a um espaço dedicado a eventos esportivos e culturais com fins lucrativos. A licença Nº
                137/2021, através do processo Nº 52.952/2019” foi concedida ainda em setembro de 2021 pela Prefeitura de
                Cabo Frio após tramitar pelas secretarias pertinentes e, passar por análises jurídica, urbanística,
                ambiental e da titularidade. O prefeito José Bonifácio declarou através das redes sociais apoio a essa
                ampliação pois a mesma estaria empregando cerca de 250 jovens.

                <br><br>


            </p>

