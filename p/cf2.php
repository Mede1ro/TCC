        <div class="conteudo-conflitos-texto">

            <p>

                <br><br>
                Ainda em julho as obras foram suspensas por recomendação do MPF para aguardarem a audiência pública que
                aconteceu no dia 12 de agosto em que o Procurador da República Leandro Mitidieri Figueiredo convocou a
                presença dos representantes da Prefeitura Municipal de Cabo Frio, Câmara Municipal de Cabo Frio, o INEA
                e Shopping Park Lagos, no qual o principal objetivo da audiência foi discutir o procedimento n.
                1.30.009.000153/2022-04, que apura “possível cercamento irregular de área de restinga próximo a Laguna
                de Araruama praticado por representantes do Shopping Park Lagos para destruição da vegetação local e
                ampliação do estacionamento do referido shopping”.
                <br><br>
                A Prefeitura Municipal de Cabo Frio não enviou nenhum representante para a audiência, demonstrando
                descaso com a população, que denunciou a construção em território de proteção ambiental e com o meio
                ambiente.
                <br><br>
                A Prefeitura de Cabo Frio publicou através do site oficial dela o seguinte texto sobre a medida
                compensatória do Shopping Park Lagos: “Cumprindo uma solicitação de extensão da compensação ambiental, o
                Shopping Park Lagos iniciou, nesta segunda-feira (26), a remoção do que restou do material dos antigos
                quiosques, que ficam instalados na localidade conhecida como Bolsão da Juju, na altura da Praia das
                Dunas, em Cabo Frio. As ações vão continuar até que todos os objetos sejam removidos.
                <br><br>
                Ainda por conta do cumprimento, já foi iniciada a revitalização da Faixa Marginal de Proteção do bairro
                Palmeiras, com espécies nativas e típicas do ecossistema local, com o plantio de centenas de mudas de
                árvores.”
                Segundo o site local “Jornal de Sábado": Outras ações ainda serão realizadas pelo Shopping Park Lagos
                visando a compensações ambientais, como a recuperação da Rua João Luiz Gago, incluindo iluminação e
                calçada, que beneficiará os moradores da localidade com uma nova orla.”
            </p>

